// What is JavaScript?
// JavaScript is a high-level, interpreted programming language
// that is used to make web pages interactive.
//
// It can:
// 1. Change HTML content.
// 2. Change CSS styles.
// 3. Handle user events.
// 4. Make API requests.
// 5. Build servers using Node.js.

// =========================================
// Why Learn JavaScript?
// =========================================

// • Runs in all modern web browsers.
// • Used for both Frontend and Backend development.
// • Beginner-friendly.
// • Huge developer community.
// • One of the most popular programming languages.

// =========================================
// Ways to Add JavaScript
// =========================================

// 1. Inline JavaScript
// <button onclick="alert('Hello')">Click</button>

// 2. Internal JavaScript
// <script>
//     console.log("Hello");
// </script>

// 3. External JavaScript (Recommended)
// <script src="script.js"></script>

// =========================================
// First JavaScript Program
// =========================================

